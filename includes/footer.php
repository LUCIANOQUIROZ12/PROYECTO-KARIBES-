        document.addEventListener('DOMContentLoaded', () => {
            
            /* 1. COMPORTAMIENTO DEL HEADER AL HACER SCROLL (Glassmorphism) */
            const header = document.getElementById('header');
            const handleScroll = () => {
                if (window.scrollY > 50) {
                    header.classList.add('scrolled');
                } else {
                    header.classList.remove('scrolled');
                }
            };
            window.addEventListener('scroll', handleScroll, { passive: true });
            handleScroll();

            /* 2. MENÚ MÓVIL (Interacción Hamburguesa a 'X') */
            const mobileBtn = document.getElementById('mobile-menu-btn');
            const navLinks = document.getElementById('nav-links');
            const body = document.body;
            
            if(mobileBtn && navLinks) {
                mobileBtn.addEventListener('click', () => {
                    mobileBtn.classList.toggle('open');
                    navLinks.classList.toggle('active');
                    body.style.overflow = navLinks.classList.contains('active') ? 'hidden' : '';
                });

                const links = document.querySelectorAll('.nav-links li a');
                links.forEach(link => {
                    link.addEventListener('click', () => {
                        mobileBtn.classList.remove('open');
                        navLinks.classList.remove('active');
                        body.style.overflow = ''; 
                    });
                });
            }

            /* 3. LÓGICA DEL SLIDER DE TESTIMONIOS */
            let currentTestimonial = 1;
            const totalTestimonials = 3; 
            const dots = document.querySelectorAll('.slider-dot');
            let sliderInterval;

            window.showTestimonial = function(index) {
                for(let i=1; i<=totalTestimonials; i++) {
                    const el = document.getElementById(`testimonio-${i}`);
                    if(el) el.classList.remove('active');
                }
                dots.forEach(dot => dot.classList.remove('active'));
                
                const selectedEl = document.getElementById(`testimonio-${index}`);
                if(selectedEl) {
                    selectedEl.classList.add('active');
                    if(dots[index-1]) dots[index-1].classList.add('active');
                    currentTestimonial = index;
                }

                resetInterval();
            };

            const startInterval = () => {
                sliderInterval = setInterval(() => {
                    let next = currentTestimonial + 1;
                    // Detectar dinámicamente cuántos testimonios reales hay en el DOM
                    const realTotal = document.querySelectorAll('.testimonial-item').length;
                    if(next > realTotal) next = 1;
                    showTestimonial(next);
                }, 6000);
            };

            const resetInterval = () => {
                clearInterval(sliderInterval);
                startInterval();
            };

            if(document.querySelector('.testimonial-slider')) {
                startInterval();
            }

            /* * NOTA DEL ARQUITECTO: 
             * El script que prevenía el envío de formularios (e.preventDefault) 
             * HA SIDO ELIMINADO intencionalmente. Ahora tus formularios PHP 
             * se enviarán a la base de datos de manera correcta y segura.
             */
        });
    </script>
